package com.alejandro.practica5_2_3;

public class ItemSolar {
    private String nombre;
    private int imagen;

    public ItemSolar(String nombre, int imagen) {
        this.nombre = nombre;
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public int getImagen() {
        return imagen;
    }
}
